package com.objet.lofteurs;

/**
 * Classe représentant une case du loft.
 * 
 * @author Denis
 */
public class Case {

	private final int x;
	private final int y;

	/**
	 * Instancie une nouvelle instance de la classe Case.
	 * 
	 * @param x
	 * @param y
	 */
	public Case(int x, int y) {
		this.x = x;
		this.y = y;
	}
}
